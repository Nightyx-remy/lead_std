#ifndef LEAD_STD_PRIMITIVES_H
#define LEAD_STD_PRIMITIVES_H

// Integer primitives
typedef signed char i8;
typedef short i16;
typedef int i32;
typedef long long i64;
typedef int isize;

// Unsigned integer primitives
typedef unsigned char u8;
typedef unsigned short u16;
typedef unsigned int u32;
typedef unsigned long u64;
typedef unsigned int usize;

// Boolean
#define true 1
#define false 0

typedef int bool;

#endif //LEAD_STD_PRIMITIVES_H
